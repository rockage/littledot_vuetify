func (s T_Selenium) SwitchPage(w int) bool {
	windows, _ := Selenium.wd.WindowHandles()
	var handle string = ""
	for k, v := range windows {
		if k == w {
			handle = v
		}
	}
	if handle != "" {
		s.wd.SwitchWindow(windows[w])
		return true
	} else {
		return false
	}
}

